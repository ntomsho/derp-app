export function randomResourceItem(resource) {
    switch (resource) {
        case "Weapon Oil":
            return `Weapon Oil of ${random([random(ELEMENTS_OF), random(GERUNDS)])}`;
        case "Animal Totem":
            return `${randomAnimal()} with ${random(MUTATIONS)} Totem`
        case "Scroll of Power":
            return `Scroll of ${random([random(GERUNDS), random(ELEMENTS)])}`
        case "Command Scroll":
            return `Scroll of ${random(COMMANDS)}`
        case "Songbook":
            const noteType = random(["Element", "Verb"])
            const song = noteType === "Element" ? `${random(SONGS)} of ${random(ELEMENTS_OF)}` : `${random(GERUNDS)} ${random(SONGS)}`
            return `Songbook of ${song}`
        case "Holy Symbol":
            return `Holy Symbol of ${random(SKILLS)}`
        case "Alchemical Ingredient":
            return `${random([random(BASES) + " Base", random(ELEMENTS_OF) + " Catalyst", random(GERUNDS)])}`
        default:
            return randomResourceItem(random(RESOURCE_ITEMS))
    }
}


export function randomTag(cClass) {
    switch (cClass) {
        case "Battlebro":
            const specialCat = random([GERUNDS, ELEMENTS_OF])
            if (specialCat === GERUNDS) {
                return random(specialCat) + " " + random(WEAPONS.slice(0,18));
            } else {
                return random(WEAPONS.slice(0,18)) + " of " + random(specialCat);
            }
        case "Bowslinger":
            return random([random(ELEMENTS), random(GERUNDS)]) + " Ammo";
        case "Hippy":
            return random([random(MUTATIONS) + " (Gift)", randomAnimal() + " Animal Form"]);
        case "Knight of Tushuze":
            return "Blessing of " + random(SKILLS);
        case "Minstrel":
            return random([random(SONGS) + " (Song)", random([random(ELEMENTS), random(GERUNDS)] + " Note")]);
        case "Mixologist":
            return random([random(BASES) + " (Base)", random([random(GERUNDS), random(ELEMENTS)] + " (Catalyst)")]);
        case "Ne'erdowell":
            return randomMagicItem();
        case "Ragesmasher":
            return randomAnimal();
        case "Runegoon":
            return random(random([VERBS, ELEMENTS]));
        case "Verbpriest":
            return random(COMMANDS);
        case "Wizcaster":
            const wordCat = random(["Form", "Element", "Verb"]);
            switch (wordCat) {
                case "Form":
                    return random(FORMS) + " (Form)";
                case "Element":
                    return random(ELEMENTS) + " (Element)";
                default:
                    return random(VERBS) + " (Verb)";
            }
        case "Zoomaster":
            const mutation = random(MUTATIONS);
            if (ADJECTIVE_MUTATIONS.includes(mutation)) {
                return mutation + " " + randomAnimal();
            } else {
                return randomAnimal() + " with " + mutation;
            }
        default:
            return;
    }
}