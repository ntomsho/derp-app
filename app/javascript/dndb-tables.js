export function random(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

export function randomRace() {
    if (Math.floor(Math.random() * 100) <= 66) {
        return "Human";
    } else {
        const trait1 = random(RACE_TRAITS);
        let trait2 = random(RACE_TRAITS);
        while (trait1 === trait2) {
            trait2 = random(RACE_TRAITS);
        }
        return [random(RACE_TRAITS), random(RACE_TRAITS)];
    }
}

export function randomAnimal() {
    return random(random([AERIAL_CREATURES, AQUATIC_CREATURES, BUG_CREATURES, PREDATOR_CREATURES, PREY_CREATURES, DOMESTICATED_CREATURES]));
}

